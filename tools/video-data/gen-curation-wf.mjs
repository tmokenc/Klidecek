#!/usr/bin/env node
// Generates tools/video-data/curation-wf.js — a reasoning-only Workflow that
// (1) maps the known @kocotom videos onto subtopics and (2) proposes reputable
// YouTube search intents per subtopic. No network in the workflow; real video
// IDs are resolved later by yt-dlp (tools/video-data/resolve.mjs).
import fs from 'node:fs';
const DIR = '/home/tmokenc/workspace/vut/aio/tools/video-data';

const catalogue = JSON.parse(fs.readFileSync(DIR + '/catalogue.json', 'utf8'));
const COURSES = {};
for (const r of catalogue) {
  COURSES[r.course] ??= { name: r.courseName, topics: {} };
  COURSES[r.course].topics[r.topic] ??= { title: r.topicTitle, subs: [] };
  COURSES[r.course].topics[r.topic].subs.push({ id: r.sub, title: r.subTitle });
}
// flatten topics object -> array for stable iteration
const COURSES_ARR = {};
for (const [cid, c] of Object.entries(COURSES)) {
  COURSES_ARR[cid] = { name: c.name, topics: Object.entries(c.topics).map(([id, t]) => ({ id, title: t.title, subs: t.subs })) };
}

const kocoLines = fs.readFileSync(DIR + '/kocotom-videos.tsv', 'utf8').trim().split('\n');
const KOCOTOM = kocoLines.map(l => { const [id, dur, ...t] = l.split('\t'); return { id, dur: Math.round(+dur), title: t.join('\t') }; });

// Courses whose subjects @kocotom (theory/math/algorithms) plausibly covers.
const KOCO_COURSES = ['TIN', 'FLP', 'PRL', 'MSP', 'SUI', 'AVS', 'UPA'];

// Reputable educational YouTube channels the resolver will accept.
const ALLOWLIST = [
  'Computerphile', 'Numberphile', '3Blue1Brown', 'Reducible', 'Spanning Tree', 'Art of the Problem',
  'Khan Academy', 'MIT OpenCourseWare', 'Stanford Online', 'CS50', 'CrashCourse', 'Veritasium',
  'Welch Labs', 'Two Minute Papers', 'Sebastian Lague', 'Branch Education', 'Mathologer', 'Steve Brunton',
  'mathematicalmonk', 'ritvikmath', 'StatQuest with Josh Starmer', 'Serrano.Academy', 'Visually Explained',
  'Ben Eater', 'EEVblog', 'Asianometry', 'Core Dumped', 'Low Level', 'Low Level Learning', 'Jacob Sorber',
  'Robert Feranec', "Phil's Lab", 'Hussein Nasser', 'Gaurav Sen', 'ByteByteGo', 'PowerCert Animated Videos',
  'Practical Networking', 'NetworkChuck', 'IBM Technology', 'Google for Developers', 'ArjanCodes',
  'CodeAesthetic', 'Continuous Delivery', 'Fireship', 'The Net Ninja', 'Web Dev Simplified', 'Traversy Media',
  'Academind', 'Kevin Powell', 'LiveOverflow', 'John Hammond', 'IppSec', 'PwnFunction', 'Cyrill Gössi',
  'Andrej Karpathy', 'Yannic Kilcher', 'sentdex', 'DeepLearningAI', 'NNgroup', 'Nielsen Norman Group',
  'AJ&Smart', 'Android Developers', 'Apple Developer', 'Philipp Lackner', 'CodeWithChris', 'freeCodeCamp.org',
  'CMU Database Group', 'Hnasr', 'Spanning Tree', 'Tom Scott', 'Lex Fridman', 'PBS Infinite Series',
  'Eli the Computer Guy', 'Hardware Unboxed', 'Gary Explains', 'Computer Science Lessons', 'Neso Academy',
  'Abdul Bari', 'WilliamFiset', 'Back To Back SWE', 'Errichto', 'Tushar Roy - Coding Made Simple',
  'mCoding', 'Tsoding', 'The Coding Train', 'Socratica', 'Derek Banas', 'Bisqwit',
];

const out = [];
out.push(`// AUTO-GENERATED by gen-curation-wf.mjs — do not edit by hand.`);
out.push(`export const meta = {`);
out.push(`  name: 'video-curation',`);
out.push(`  description: 'Map @kocotom lectures onto subtopics and propose reputable YouTube search intents (reasoning only; ids resolved later by yt-dlp).',`);
out.push(`  phases: [{ title: 'Kocotom map' }, { title: 'Reliable search' }],`);
out.push(`};`);
out.push(`const COURSES = ${JSON.stringify(COURSES_ARR)};`);
out.push(`const KOCOTOM = ${JSON.stringify(KOCOTOM)};`);
out.push(`const KOCO_COURSES = ${JSON.stringify(KOCO_COURSES)};`);
out.push(`const ALLOWLIST = ${JSON.stringify(ALLOWLIST)};`);
out.push(String.raw`
const KOCO_SCHEMA = {
  type: 'object', additionalProperties: false,
  properties: {
    course: { type: 'string' },
    maps: { type: 'array', items: {
      type: 'object', additionalProperties: false,
      properties: { topic: { type: 'string' }, sub: { type: 'string' }, videoId: { type: 'string' }, why: { type: 'string' } },
      required: ['topic', 'sub', 'videoId', 'why'],
    } },
  },
  required: ['course', 'maps'],
};
const REL_SCHEMA = {
  type: 'object', additionalProperties: false,
  properties: {
    course: { type: 'string' },
    picks: { type: 'array', items: {
      type: 'object', additionalProperties: false,
      properties: {
        topic: { type: 'string' }, sub: { type: 'string' },
        query: { type: 'string' }, channel: { type: 'string' },
        keywords: { type: 'array', items: { type: 'string' } }, why: { type: 'string' },
      },
      required: ['topic', 'sub', 'query', 'channel', 'keywords', 'why'],
    } },
  },
  required: ['course', 'picks'],
};

function subsList(c) {
  return c.topics.flatMap(t => t.subs.map(s => t.id + '/' + s.id + ' :: ' + s.title + '  (téma: ' + t.title + ')')).join('\n');
}

phase('Kocotom map');
const kocoList = KOCOTOM.map(v => v.id + '  [' + v.dur + 's]  ' + v.title).join('\n');
const kocoResults = await parallel(KOCO_COURSES.map(cid => () => {
  const c = COURSES[cid];
  if (!c) return null;
  const prompt = [
    "You map existing Czech YouTube lectures by 'Tomáš Kocourek' (channel @kocotom) onto subtopics of ONE university course in a Czech CS exam-prep app.",
    '',
    'COURSE: ' + cid + ' — ' + c.name,
    '',
    'SUBTOPICS (format  topicId/subId :: subtitle  (téma: topicTitle)):',
    subsList(c),
    '',
    'AVAILABLE VIDEOS (format  VIDEOID  [durationSeconds]  Title) — real fixed ids, copy verbatim:',
    kocoList,
    '',
    'TASK: For each video genuinely relevant to one or more subtopics of THIS course, output a mapping {topic, sub, videoId, why}.',
    '- These are long comprehensive lecture recordings; attach a video to the single best-matching subtopic, or a few if it squarely covers them.',
    '- One entry per (subtopic, video). A video may map to multiple subtopics across topics.',
    '- ONLY use videoId values copied EXACTLY from the AVAILABLE VIDEOS list. Never invent or alter an id.',
    '- topic and sub must be EXACT ids from the SUBTOPICS list (the part before/after the slash).',
    "- Be precise: a wrong topical match is worse than no match. If nothing fits, return an empty maps array.",
    "- 'why' = one short Czech clause.",
    'Return {course, maps}.',
  ].join('\n');
  return agent(prompt, { label: 'koco:' + cid, phase: 'Kocotom map', schema: KOCO_SCHEMA });
}));

phase('Reliable search');
const relResults = await parallel(Object.keys(COURSES).map(cid => () => {
  const c = COURSES[cid];
  const prompt = [
    'You are a CS educator curating ONE excellent YouTube explainer video per worthy subtopic of a university course, for a Czech exam-prep app. Videos are fetched LATER by an automated yt-dlp search using YOUR query — so your job is to (a) choose which subtopics deserve a video and (b) write a precise search query and name the channel.',
    '',
    'COURSE: ' + cid + ' — ' + c.name,
    '',
    'SUBTOPICS (topicId/subId :: subtitle (téma: topicTitle)):',
    subsList(c),
    '',
    'REPUTABLE CHANNELS — the resolver ONLY accepts a video whose channel matches the one you name here, and that channel MUST be one of these (or an obvious variant):',
    ALLOWLIST.join(', '),
    '',
    'TASK: For each subtopic where a well-known high-quality video from a reputable channel almost certainly exists, output a pick:',
    '- query: a YouTube search query likely to surface that exact video. INCLUDE the channel name, e.g. "Computerphile RSA encryption", "3Blue1Brown gradient descent", "Ben Eater error detection".',
    '- channel: the exact channel name (from the list above).',
    '- keywords: 2-5 lowercase words that MUST appear in the correct video title (used to score search hits).',
    "- why: one short clause on relevance.",
    'RULES:',
    '- QUALITY OVER COVERAGE. Skip any subtopic without a clearly-existing great video. Many/most subtopics will have none — that is expected; do NOT force generic videos.',
    '- One pick per subtopic at most (the single best video).',
    '- Prefer concept-explainer channels (Computerphile, 3Blue1Brown, Ben Eater, Reducible, StatQuest, Hussein Nasser, ByteByteGo, NNgroup, Fireship, etc.). Match the channel to the domain.',
    '- The topic must be squarely in scope; never attach an off-topic but famous video.',
    'Return {course, picks}.',
  ].join('\n');
  return agent(prompt, { label: 'rel:' + cid, phase: 'Reliable search', schema: REL_SCHEMA });
}));

return { koco: kocoResults.filter(Boolean), rel: relResults.filter(Boolean) };
`);

fs.writeFileSync(DIR + '/curation-wf.js', out.join('\n'));
console.log('wrote curation-wf.js (', out.join('\n').length, 'bytes )');
console.log('courses:', Object.keys(COURSES_ARR).length, '| kocotom videos:', KOCOTOM.length, '| allowlist:', ALLOWLIST.length);
