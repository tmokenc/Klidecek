#!/usr/bin/env node
// Generates refine-wf.js — a reasoning-only Workflow that scans the videos
// attached to each course and flags ONLY those whose subject is "too specific"
// for that course and is squarely the specialization of a DIFFERENT course
// (which must have a matching subtopic). Like the Spectre&Meltdown case: a
// CPU-security attack video on an architecture course → belongs in the
// hardware-security course; the origin only cross-references it.
// Shared/common/foundational content (appears in several courses legitimately)
// is left alone. Output drives a human-reviewed move list, not auto-apply.
import fs from 'node:fs';
const DIR = '/home/tmokenc/workspace/vut/aio/tools/video-data';
const catalogue = JSON.parse(fs.readFileSync(DIR + '/catalogue.json', 'utf8'));
const videos = JSON.parse(fs.readFileSync(DIR + '/final-videos.json', 'utf8'));

const courseName = {};
for (const r of catalogue) courseName[r.course] = r.courseName;
const courseList = Object.entries(courseName).map(([id, n]) => `${id} — ${n}`).join('\n');

// compact catalogue: course :: topic/sub :: subtitle  (so agents can name a real target subtopic)
const catByCourse = {};
for (const r of catalogue) (catByCourse[r.course] = catByCourse[r.course] || []).push(`${r.topic}/${r.sub} :: ${r.subTitle}`);
const catalogueText = Object.entries(catByCourse).map(([c, arr]) => `### ${c} — ${courseName[c]}\n` + arr.join('\n')).join('\n\n');

// videos per course
const vidByCourse = {};
for (const v of videos) (vidByCourse[v.course] = vidByCourse[v.course] || []).push(v);

const out = [];
out.push(`// AUTO-GENERATED by gen-refine-wf.mjs — do not edit by hand.`);
out.push(`export const meta = { name: 'video-refine', description: 'Flag videos that are too specific for their course and belong to a specialist course (Spectre-style cross-reference).', phases: [{ title: 'Refine' }] };`);
out.push(`const COURSE_NAME = ${JSON.stringify(courseName)};`);
out.push(`const COURSE_LIST = ${JSON.stringify(courseList)};`);
out.push(`const CATALOGUE = ${JSON.stringify(catalogueText)};`);
out.push(`const VID_BY_COURSE = ${JSON.stringify(vidByCourse)};`);
out.push(String.raw`
const SCHEMA = {
  type: 'object', additionalProperties: false,
  properties: {
    course: { type: 'string' },
    moves: { type: 'array', items: {
      type: 'object', additionalProperties: false,
      properties: {
        videoId: { type: 'string' }, sub: { type: 'string' },
        targetCourse: { type: 'string' }, targetTopic: { type: 'string' }, targetSub: { type: 'string' },
        reason: { type: 'string' },
      },
      required: ['videoId', 'sub', 'targetCourse', 'targetTopic', 'targetSub', 'reason'],
    } },
  },
  required: ['course', 'moves'],
};

phase('Refine');
const results = await parallel(Object.keys(VID_BY_COURSE).map(cid => () => {
  const vids = VID_BY_COURSE[cid];
  const vlist = vids.map(v => v.videoId + ' | ' + v.topic + '/' + v.sub + ' | ' + v.title + ' | ' + v.channel).join('\n');
  const prompt = [
    "You audit the supplementary YOUTUBE VIDEOS attached to subtopics of ONE university course in a Czech CS exam-prep app. You look for a video whose SUBJECT is 'too specific' for THIS course and is squarely the SPECIALIZATION of a DIFFERENT course — e.g. a CPU-security *attack* video (Spectre/Meltdown) sitting on a computer-architecture course, when a hardware-security course exists. Such a video should be MOVED to the specialist course (which must have a fitting subtopic); this course then only cross-references it.",
    '',
    'THIS COURSE: ' + cid + ' — ' + COURSE_NAME[cid],
    '',
    'ITS VIDEOS  (videoId | topic/sub | title | channel):',
    vlist,
    '',
    'ALL COURSES (id — name):',
    COURSE_LIST,
    '',
    'EVERY COURSE’S SUBTOPICS (to name an exact target topic/sub that specializes in the subject):',
    CATALOGUE,
    '',
    'RULES — be CONSERVATIVE, default is LEAVE:',
    '- Flag a video ONLY if its subject is clearly OUTSIDE this course’s domain and is the CORE specialization of another course.',
    '- SHARED / common / foundational content that legitimately appears in several courses (probability in stats AND ML; AES in crypto AND security; REST in web AND enterprise IS) is FINE — do NOT move it.',
    '- A move is valid only if the target course genuinely specializes in the subject AND has a specific matching subtopic — give its EXACT targetCourse, targetTopic, targetSub ids from the list above.',
    '- Litmus test: "This course teaches X. This video is about Y, which is course Z’s specialty, not X."',
    '- Most courses will have ZERO moves. Quality over quantity. Never invent ids.',
    'Return {course, moves}.',
  ].join('\n');
  return agent(prompt, { label: 'refine:' + cid, phase: 'Refine', schema: SCHEMA });
}));
return results.filter(Boolean);
`);
fs.writeFileSync(DIR + '/refine-wf.js', out.join('\n'));
console.log('wrote refine-wf.js | courses with videos:', Object.keys(vidByCourse).length);
